package conditionalstatements;

public class switchcase {

	public static void main(String[] args) {
		
		int i=3;
		switch(i) {
		case 1:
			System.out.println("1st case");
			break;
		case 2:
			System.out.println("2nd case");
			break;
		case 3:
			System.out.println("3rd case");
			break;
		default:
			System.out.println("defalut case");
			break;
		}
	}

}
