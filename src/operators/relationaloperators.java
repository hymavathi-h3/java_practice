package operators;

public class relationaloperators {

	public static void main(String[] args) {
		
		int i=234;
		int j=567;
		
		System.out.println(i>j);
		System.out.println(i<j);
		System.out.println(i==j);
		System.out.println(i!=j);
		System.out.println(i>=j);
		System.out.println(i<=j);
	}

}
