package operators;

public class unaryoprator {
	
	public static void main(String[] args) {
		
		int i=267;
		int j=456;
		
		System.out.println(i);
		System.out.println(++i);
		System.out.println(--i);
		System.out.println(i++);
		System.out.println(i--);
		System.out.println(i);
		

		System.out.println(j);
		System.out.println(++j);
		System.out.println(--j);
		System.out.println(j++);
		System.out.println(j--);
		System.out.println(j);
		
		
	}

}
