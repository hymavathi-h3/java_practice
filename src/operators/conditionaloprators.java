package operators;

public class conditionaloprators {

	public static void main(String[] args) {
		
		int i=200;
		int j =300;
		
		System.out.println(i>j && i==j);
		System.out.println(i<j && i==j);
		System.out.println(i>j && i!=j);
		System.out.println(i<j && i!=j);
		
		System.out.println(i>j || i==j);
		System.out.println(i<j || i==j);
		System.out.println(i>j || i!=j);
		System.out.println(i<j || i!=j);
	}

}
