package variables;

public class basicinfo {
	static String name = "hyma";

	static String NAME = "hymavathi";
	
	static String lower="all letters should be in lower case";
	
	static String lowerCase="words should be in camel case";

	public static void main(String[] args) {

		int i; // defining variable
		
		i = 5; // intialization
		
		i = 456; // overriding value
		
		System.out.println(i);
		
		char c = 'j'; // variable can be assigned like this also
		
		int object=32;
		
		System.out.println(name);

		System.out.println(NAME);
		
		System.out.println(lower);
		
		System.out.println(lowerCase);

	}

}
